_IMPL_CACHE: dict[tuple[str, str], Any] = {}
_IMPL_IMPORT_ERRORS: dict[tuple[str, str], BaseException] = {}
_WARNED_IMPORT_ERRORS: set[tuple[str, str]] = set()

def _impl_dir(arch: str) -> Path:
    return Path(__file__).resolve().parent / '_impls' / arch

def _load_impl(arch: str, module_name: str) -> Any:
    key = (arch, module_name)
    if key in _IMPL_CACHE:
        return _IMPL_CACHE[key]
    path = str(_impl_dir(arch))
    if path not in sys.path:
        sys.path.insert(0, path)
    module = importlib.import_module(module_name)
    _IMPL_CACHE[key] = module
    return module

def _preload_kda_impls(strict: bool = False) -> None:
    """Import promoted wrappers before monkey-patching SGLang."""
    for by_arch in _IMPL_MODULES.values():
        for arch, module_name in by_arch.items():
            key = (arch, module_name)
            if key in _IMPL_CACHE or key in _IMPL_IMPORT_ERRORS:
                continue
            try:
                _load_impl(arch, module_name)
            except Exception as exc:  # noqa: BLE001
                _IMPL_IMPORT_ERRORS[key] = exc
                if strict:
                    raise
                warnings.warn(
                    f'kda_kernels failed to preload {module_name} for {arch}: {exc!r}'
                )

def _iter_tensors(value: Any):
    if torch is not None and isinstance(value, torch.Tensor):
        yield value
    elif isinstance(value, (tuple, list)):
        for item in value:
            yield from _iter_tensors(item)
    elif isinstance(value, dict):
        for item in value.values():
            yield from _iter_tensors(item)

def _select_arch(args: tuple[Any, ...], kwargs: dict[str, Any]) -> str | None:
    forced = os.environ.get('KDA_FORCE_ARCH')
    if forced in _SUPPORTED_ARCHES:
        return forced
    if torch is None:
        return None
    for tensor in _iter_tensors(args):
        if getattr(tensor, 'is_cuda', False):
            try:
                return _ARCH_BY_CAPABILITY.get(tuple(torch.cuda.get_device_capability(tensor.device)))
            except Exception:
                return None
    for tensor in _iter_tensors(kwargs):
        if getattr(tensor, 'is_cuda', False):
            try:
                return _ARCH_BY_CAPABILITY.get(tuple(torch.cuda.get_device_capability(tensor.device)))
            except Exception:
                return None
    return None

