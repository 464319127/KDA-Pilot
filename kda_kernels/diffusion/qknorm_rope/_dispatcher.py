        if key not in _WARNED_IMPORT_ERRORS:
            _WARNED_IMPORT_ERRORS.add(key)
            warnings.warn(
                f'kda_kernels falling back for {fn_name}: {module_name} '
                f'failed to preload for {arch}: {_IMPL_IMPORT_ERRORS[key]!r}'
            )
